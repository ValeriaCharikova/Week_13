function showMessage() {
  let name = prompt("Как тебя зовут?");
  alert(`Привет, ${name}!`);
}

function addition() {
  let a = prompt("Первое число");
  let b = prompt("Второе число");
  alert(+a + +b);
}

function subtraction() {
  let a = prompt("Первое число");
  let b = prompt("Второе число");
  alert(a - b);
}

function multiplication() {
  let a = prompt("Первое число");
  let b = prompt("Второе число");
  alert(a * b);
}

function division() {
  let a = prompt("Первое число");
  let b = prompt("Второе число");
  alert(a / b);
}
